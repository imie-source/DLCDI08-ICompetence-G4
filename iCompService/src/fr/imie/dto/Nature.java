package fr.imie.dto;

/**
 * Nature Object
 * 
 * @author imie
 * @version 1.0
 *
 */
public class Nature {

	private int id;
	private String libelle;
	private String code;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getLibelle() {
		return libelle;
	}
	public void setLibelle(String libelle) {
		this.libelle = libelle;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	

}
